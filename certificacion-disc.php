                                Historia del DISC
                                DISC es un modelo teórico de análisis de comportamiento desarrollado en la década de los '40 por el Prof. William Marston (1893-1947) de la Universidad de Harvard. En 1928 Marston publicó el libro "Emociones de las Personas Normales" donde expuso formalmente la Teoría DISC y en 1931 publico su segundo libro acerca de la teoría titulado: "Psicología Integrativa". Según DISC, todo ser humano muestra una combinación de 4 tendencias generales de comportamiento bien diferenciadas y complementarias: Dominancia, Influencia, Estabilidad y Cumplimiento (o conformidad a procedimientos). El modelo se llama así por sus siglas en inglés; Dominance, Influence, Stability y Compliance, DISC. Los 4 estilos mencionados en los textos hipocráticos son: Cólerico, Sangüineo, Flemá-tico y Melancólico y son perfectamente análogos a los definidos posteriormente por Marston.</p>
                            <p>

                                Después de Hipócrates, el segundo hito importante en la evolución el DISC son los estudios de Carl Gustav Jung (1875-1961), cuando analiza cómo se dan los procesos de adaptación y orientación a los mundos externo e interno a partir de las preferencias individuales entre pares de opuestos (actitud extrovertida e introvertida) y su combinación con los tipos funcionales: pensamiento, sentimiento, sensación e intuición.
                            </p>
